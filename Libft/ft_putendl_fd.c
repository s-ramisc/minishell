#include "libft.h"

void	ft_putendl_fd(char *s, int fd)
{
	size_t	i;
	char	x;

	if (!s)
		return ;
	i = 0;
	while (i != ft_strlen(s))
	{
		x = s[i];
		write(fd, &x, 1);
		i++;
	}
	write(fd, "\n", 1);
}
